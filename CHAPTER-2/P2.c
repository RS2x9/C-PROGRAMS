// ARITHMETIC OPERATORS
#include<stdio.h>
int main()
{
    printf("\n") ; 
    /* modulas operator: used to find remainder
    only works on integer data
    */

    printf("%d \n",3%2) ; 
    printf(" %d \n",-3%2) ;

    //   / : division operator

    printf("%d \n",4/2); 
    printf("%f \n",4/2.0);
    printf("%d \n", 2/3); 
    printf("%f \n",2.0/3);
    return 0;
}