// 1. Variable Names (Identifiers)
// Valid Characters: Variable names can consist of letters (A-Z or a-z), digits (0-9), and underscores (_).

// Example: int age; or float price_2024;
// First Character Rule: The first character of a variable name must be a letter or an underscore. It cannot be a digit.

// Valid: int age;, int _value;
// Invalid: int 9months; (starts with a digit)
// No Spaces: Variable names cannot contain spaces. Use an underscore (_) or camelCase to separate words in a variable name.

// Valid: int user_age;, float totalPrice;
// Invalid: int total Price; (spaces not allowed)
// No Special Characters: Variable names cannot include special characters like !, @, #, etc.

// Invalid: int my$value;
// 2. Reserved Keywords
// You cannot use C++ reserved keywords (such as int, return, class, etc.) as variable names.

// Invalid: int float = 5; (cannot use a keyword as a variable)
// 3. Case Sensitivity
// C++ is case-sensitive, so age and Age are considered two different variables.

